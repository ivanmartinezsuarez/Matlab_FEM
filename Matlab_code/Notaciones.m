Para mallados P1:

T es un tablero de dimension NbTriang x 3:
Cada fila indica los tres vertices del triangulo.

Arista 1 ==> Punto 1 y Punto 2.
Arista 2 ==> Punto 2 y Punto 3.
Arista 3 ==> Punto 3 y Punto 1.

Para mallados P2

T es un tablero de dimension NbTriang x 6:
Cada fila indica los tres vertices del triangulo asi
como los tres puntos intermedios.

Para el triangulo k:
T(k,1) => Primer vertice.
T(k,2) => Segundo vertice.
T(k,3) => Tercer vertice.
T(k,4) => Punto intermedio el vertice T(k,1) y T(k,2).
T(k,5) => Punto intermedio el vertice T(k,2) y T(k,3).
T(k,6) => Punto intermedio el vertice T(k,3) y T(k,1).

